package com.company.common;

public class Pack {
    private String argA;
    private String argB;
    public Pack(String argA, String argB){
        this.argA = argA;
        this.argB = argB;
    }

    public String getArgA() {
        return argA;
    }

    public String getArgB() {
        return argB;
    }
}
